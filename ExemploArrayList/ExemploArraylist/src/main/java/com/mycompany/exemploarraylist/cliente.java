package com.mycompany.exemploarraylist;

/**
 *
 * @author lucas
 */
public class cliente {
    private int id;
    private String name;
    private int idade;
  public cliente( int id, String name, int idade){
    this.id = id;
    this.name = name;
    this.idade = idade;
  }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
