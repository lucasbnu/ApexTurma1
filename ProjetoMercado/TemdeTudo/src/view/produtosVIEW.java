    public void HabilitaCampos( boolean habilitado){
         txtdescricao.setEditable(habilitado);
         txtquantidade_estoque.setEditable(habilitado);
         txtvalor.setEditable(habilitado);
    }
    
     public void atualizaDados(String filtro){
         produtosDTO prodDTO = new produtosDTO();
         geralDAO DAO = new geralDAO();
          jTable1.setModel(DAO.retornaConsulta(prodDTO,filtro));
     } 
     
     public void limpaTela(){
             txtid_prod.setText("");
             txtquantidade_estoque.setText("");
             txtdescricao.setText("");
             txtvalor.setText("");
     }
   
