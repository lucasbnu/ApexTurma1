    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        produtosDTO prodDTO = new produtosDTO();
        if (acao != 1){
          prodDTO.setId_produto(Integer.parseInt(txtid_prod.getText()));
        }

        prodDTO.setDescricao_produto(txtdescricao.getText());
        prodDTO.setValor(Double.parseDouble(txtvalor.getText()));
        prodDTO.setQuantidade_estoque(Integer.parseInt(txtquantidade_estoque.getText()));
        geralDAO DAO = new geralDAO();
        String comando = "";
        if (acao == 1){
           comando = DAO.insereRegistro(prodDTO);
        }else if (acao == 2){
            comando =DAO.atualizaRegistro(prodDTO);
        }
        JOptionPane.showMessageDialog(null, comando);
       acao = 0;
       btnCancelar.setEnabled(false);
       btnSalvar.setEnabled(false);
       btnAlterar.setEnabled(true);
       btnExcluir.setEnabled(true);
       btnNovo.setEnabled(true);       
        HabilitaCampos(false);
    }//GEN-LAST:event_btnSalvarActionPerformed

